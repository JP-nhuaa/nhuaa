                <h2 style="margin-top: 80px">About NHUAA</h2>
                <p>New Hampshire Union Accountability Advocates (NHUAA) was established by John "JP" Dustin, a dedicated member of the International Brotherhood of Teamsters since 2017. JP has a strong background in union activities and has served as a shop steward while employed at UPS. In 2024, JP, along with two other stewards, stepped down from their roles due to a lack of support from their local leadership. Motivated by the need for better member support and transparency, JP founded NHUAA in the year of an executive board election for Local 633. The organization aims to inform union members about elections, provide a platform for raising issues, and offer support to members.</p>

                <h3>History and Background</h3>
                <p>NHUAA was founded in 2024 in response to growing concerns about the lack of transparency and accountability in union leadership. John "JP" Dustin, a member of the International Brotherhood of Teamsters, recognized the need for an organization that would advocate for fair and transparent union elections, encourage member involvement, and hold leaders accountable. Since its inception, NHUAA has grown steadily, gaining support from union members across the state.</p>

                <h3>Our Mission</h3>
                <p>Our mission is to promote transparency, accountability, and democratic principles within union leadership. We believe that fostering a culture of openness and responsibility is essential to addressing the needs and concerns of all union members. NHUAA strives to:</p>
                <ul>
                    <li>Advocate for fair and transparent union elections.</li>
                    <li>Provide resources and education to union members.</li>
                    <li>Encourage active participation in union activities and decision-making processes.</li>
                </ul>

                <h3>Who's Involved</h3>
                <p>At this time, JP is the sole team member at NHUAA. When the time comes, if you are interested in making a positive impact within your local union, contact us and help hold leadership accountable to the members.</p><br>
                
                <h4>JP Dustin</h4>
                    <p>
                        JP has been a dedicated member of the Teamsters union since 2017, serving as an alternate shop steward at UPS. Before joining the Teamsters, JP spent four years in the Marine Corps, with two deployments to Iraq and Afghanistan. It was during his time in the military that he learned the true meaning of brotherhood and solidarity. His experience as a shop steward, combined with his commitment to workers' rights and his frustration over the lack of support for union members, inspired him to establish NHUAA. JP envisions NHUAA as a crucial resource for union members who need support in standing up for their rights within their unions. Through NHUAA, he aims to empower individuals and unions alike, fostering a culture of mutual support, transparency, and accountability. JP is dedicated to creating a stronger labor movement where every worker has the tools and support needed to stand up, speak out, and drive positive change within their unions.
